sap.ui.define(["sap/ui/core/mvc/Controller"], function (Controller) {
	"use strict";
	return Controller.extend("ovly.fiori_13_t.cadastro_de_alunos.controller.Detalhe", {
		/**
		 * Called when a controller is instantiated and its View controls (if available) are already created.
		 * Can be used to modify the View before it is displayed, to bind event handlers and do other one-time initialization.
		 * @memberOf ovly.fiori_13_t.cadastro_de_alunos.view.Detalhe
		 */
		onInit: function () {
			var oRouter = this.getOwnerComponent().getRouter();
			var oRotaExibir = oRouter.getRoute("exibir");
			oRotaExibir.attachPatternMatched(this.onPatternMatched, this);
		},
		
		onPatternMatched: function (oEvent) {
			// debugger;
			var oArguments = oEvent.getParameters().arguments; // captura oEvent quando onItemPress que por sua vez cont√©m o codigo do Id como escrito no controller da Lista
			var oDataModel = this.getView().getModel(); // pega o modelo dessa View
			var sPath = oDataModel.createKey("Students", {// esse metodo cria a chamada do read
				Id: oArguments.codigo // ele devolve somente a string final; Ex.:'' "Students('AS496SAD5SA1')"
			});
			this.getView().bindElement("/" + sPath); // this: detalhe.controller; getView correspondente a this; bindElement inicializa o path que foi criado pelo oDataModel
		},
		
		onBack: function (oEvent) {
			//This code was generated by the layout editor.
			this.getOwnerComponent().getRouter().navTo("listagem");
		},
		
		toUpper: function (str) {
			if (str !== null) {
				return str.toUpperCase();
			}
		}
	});
});